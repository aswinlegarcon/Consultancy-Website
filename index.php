        <script>
            // Attach an event listener to the form
            document.getElementById('contact-form').addEventListener('submit', function (event) {
                event.preventDefault(); // Prevent default form submission

                // Get form inputs
                const name = document.querySelector('input[name="name"]').value.trim();
                const email = document.querySelector('input[name="email"]').value.trim();
                const subject = document.querySelector('input[name="subject"]').value.trim();
                const message = document.querySelector('textarea[name="message"]').value.trim();

                // Validate each field
                if (!name) {
                    alert('Please enter your name.');
                    return;
                }
                if (!email || !/\S+@\S+\.\S+/.test(email)) { // Basic email regex
                    alert('Please enter a valid email address.');
                    return;
                }
                if (!subject) {
                    alert('Please enter a subject.');
                    return;
                }
                if (!message) {
                    alert('Please enter your message.');
                    return;
                }

                // If validation passes, send the form data via EmailJS
                sendEmailJS(name, email, subject, message);
            });

            // EmailJS function
            function sendEmailJS(name, email, subject, message) {
                emailjs.send('your service id', 'your template id', {
                    from_name: name,
                    from_email: email,
                    subject: subject,
                    message: message
                }).then(() => {
                    alert('Message sent successfully!');
                    document.getElementById('contact-form').reset(); // Reset form
                }).catch(error => {
                    console.error('Failed to send message:', error);
                    alert('Failed to send message. Please try again later.');
                });
            }
        </script>



